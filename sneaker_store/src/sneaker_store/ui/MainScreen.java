    public void initAppearance(){
        Object [] title = {"ID", "Name", "Stock",  "Price"};
        DataAccessObject dap = new DataAccessObject();
        ArrayList <Shoe> Shoelist = dap.showShoes();
        Object [] [] ObjShoe = new Object[Shoelist.size()][title.length];
        jPanel2.removeAll();
        for (int i = 0; i < Shoelist.size(); i++){
            Shoe shoes = Shoelist.get(i);
            ObjShoe[i][0] = shoes.getId();
            ObjShoe[i][1] = shoes.getName();
            ObjShoe[i][2] = shoes.getStock();
            ObjShoe[i][3] = shoes.getPrice();
            
            shoeComponent shoe = new shoeComponent();
            shoe.setShoeComponent(shoes.getName(), shoes.getPrice(), shoes.getStock(), shoes.getImagePath());
            Border blackline = BorderFactory.createLineBorder(Color.black, 1, true);
            shoe.setBorder(blackline);
            jPanel2.add(shoe);
            jPanel2.revalidate();
        }
        DefaultTableModel dtm = new DefaultTableModel(ObjShoe, title);
        jTable1.setModel(dtm);
        jScrollPane1.setViewportView(jTable1);
    }

